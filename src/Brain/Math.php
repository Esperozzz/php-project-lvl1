<?php

namespace Brain\Math;

/**
 * Сложение двух чисел
 */
function addition(int $num1, int $num2): int
{
    return $num1 + $num2;
}

/**
 * Вычитание одного числа из другого
 */
function substraction(int $num1, int $num2): int
{
    return $num1 - $num2;
}

/**
 * Умножение одного числа на другое
 */
function multiplied(int $num1, int $num2): int
{
    return $num1 * $num2;
}
